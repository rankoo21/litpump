import type { Address } from "viem";

export const FACTORY_ADDRESS: Address =
  (process.env.NEXT_PUBLIC_FACTORY_ADDRESS as Address) ||
  ("0x0000000000000000000000000000000000000000" as Address);

export const isFactoryConfigured =
  FACTORY_ADDRESS && FACTORY_ADDRESS !== "0x0000000000000000000000000000000000000000";

export const COMMENTS_ADDRESS: Address =
  (process.env.NEXT_PUBLIC_COMMENTS_ADDRESS as Address) ||
  ("0x0000000000000000000000000000000000000000" as Address);

export const isCommentsConfigured =
  COMMENTS_ADDRESS && COMMENTS_ADDRESS !== "0x0000000000000000000000000000000000000000";

export const DEX_ROUTER: Address =
  (process.env.NEXT_PUBLIC_DEX_ROUTER as Address) ||
  ("0x0000000000000000000000000000000000000000" as Address);

export const DEX_FACTORY: Address =
  (process.env.NEXT_PUBLIC_DEX_FACTORY as Address) ||
  ("0x0000000000000000000000000000000000000000" as Address);

export const WLTC_ADDRESS: Address =
  (process.env.NEXT_PUBLIC_WLTC as Address) ||
  ("0x0000000000000000000000000000000000000000" as Address);

export const isDexConfigured =
  DEX_ROUTER && DEX_ROUTER !== "0x0000000000000000000000000000000000000000" &&
  DEX_FACTORY && DEX_FACTORY !== "0x0000000000000000000000000000000000000000" &&
  WLTC_ADDRESS && WLTC_ADDRESS !== "0x0000000000000000000000000000000000000000";
